import math

res = math.sqrt(144)
print("Square root of 144 is:", res)

res1 = math.factorial(5)
print("Factorial of 5 is:", res1)

res2 = math.pow(2, 3)
print("2 raised to the power 3 is:", res2)

res3 = math.gcd(48, 18)
print("GCD of 48 and 18 is:", res3)

res4 = math.pi
print("Value of pi is:", res4)

res5 = math.ceil(5/2)
print("Ceiling value of 5/2 is:", res5)